package Backend;

public class Files {
    private int id;
    private String filePath;

    Files(){

    }

    public int getId() {
        return id;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }
}

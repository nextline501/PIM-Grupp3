package Backend;

public class Img {
    private int id;
    private String url;

    Img(){

    }
    
    public int getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
